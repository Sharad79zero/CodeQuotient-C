#include <stdio.h>

int main()
{
    int wn; // wn = week number
    scanf("%d", &wn);
    if (wn == 1)
    {
        printf("Monday");
    }
    if (wn == 2)
    {
        printf("Tuesday");
    }
    if (wn == 3)
    {
        printf("Wednesday");
    }
    if (wn == 4)
    {
        printf("Thursday");
    }
    if (wn == 5)
    {
        printf("Friday");
    }
    if (wn == 6)
    {
        printf("Saturday");
    }
    if (wn == 7)
    {
        printf("Sunday");
    }
}